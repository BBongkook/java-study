package data;

public class Type01 {
	public static void main(String[] args) {
		//문자
		char c = 'a';
		
		//숫자 - 정수
		byte b = 127;
		b = -128;
		
		short sh = 123;
		
		int i = 123123123;
		
		long l = 10000000000l;
		
		// 숫자 - 실수
		float f = 0.1f;
		double db = 0.18592925424;
		
		// 논리형(불린)
		boolean bl = true;
	}
}
